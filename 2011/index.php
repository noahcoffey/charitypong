        <h1 id="aboutALS">About ALS</h1>
        <div>
            <h2>What is ALS?</h2>
            <p>Amyotrophic lateral sclerosis (ALS), often referred to as "Lou Gehrig's disease," is a progressive neurodegenerative disease that affects nerve cells in the brain and the spinal cord. Motor neurons reach from the brain to the spinal cord and from the spinal cord to the muscles throughout the body. The progressive degeneration of the motor neurons in ALS eventually lead to their death. When the motor neurons die, the ability of the brain to initiate and control muscle movement is lost. With voluntary muscle action progressively affected, patients in the later stages of the disease may become totally paralyzed. <a target="_blank" href="http://webin.alsa.org/site/PageServer?pagename=IN_1_about_als"><strong>Learn more about ALS</strong></a></p>
        </div>

